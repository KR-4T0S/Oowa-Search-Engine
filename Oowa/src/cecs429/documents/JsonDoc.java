package cecs429.documents;

public class JsonDoc {

    private String author;
    private String body;
    private String url;
    private int id;
    private String title;

    public String author() {
        return body;
    }
    
    public String getBody() {
        return body;
    }
    
    public String getURL() {
        return url;
    }

    public int getId() {
        return id;
    }

    public String getTitle() {
        return title;
    }
}
